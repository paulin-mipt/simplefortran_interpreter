PROGRAM SIMPLE_CALCULATOR
INTEGER:: arg1, arg2
 arg1 = 100
 arg2 = 10
 WRITE "The result of", arg1, "+", arg2,"is", arg1 + arg2
 WRITE "The result of", arg1, "-", arg2,"is", arg1 - arg2
 WRITE "The result of", arg1, "*", arg2,"is", arg1 * arg2
 WRITE "The result of", arg1, "/", arg2,"is", arg1 / arg2
END PROGRAM SIMPLE_CALCULATOR